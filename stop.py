import turtle
screen=turtle.Screen()
screen.setup(400,400)

t=turtle.Turtle() 
t.pensize(10)
t.color('red')
t.begin_fill()
t.forward(100)
t.left(45)
t.forward(100)
t.left(45)
t.forward(100)
t.left(45)
t.forward(100)
t.left(45)
t.forward(100)
t.left(45)
t.forward(100)
t.left(45)
t.forward(100)
t.left(45)
t.forward(100)
t.end_fill()
t.penup()
t.goto(50,80)
t.pendown()
t.color('white')
t.write("STOP", move=False, align='center', font=('Arial', 80, 'normal'))
    

